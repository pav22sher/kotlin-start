package base

/**
 * Точкой входа в программу на Kotlin является функция main
 * args-параметры командной строки (тип данных похож на typescript)
 */
fun main(args: Array<String>) {
    /**
     * В отличие от других похожих языков программирования,
     * например, Java, в Kotlin не обязательно
     * ставить после инструкции точку запятой.
     * Каждая инструкция просто размещается на новой строке.
     */
    println("Hello World!") //внутри java System.out.println(message)
}